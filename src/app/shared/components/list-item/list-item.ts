import { Component, input } from '@angular/core';

let nextListItemUid = 0;

/** Один "сегмент" строки — горизонтальное деление, свой текст и (опционально) цвет. */
export interface ListItemSegment {
  text: string;
  /** CSS-цвет текста сегмента; без значения — дефолтный (`#F9F9F9`). */
  color?: string;
}

/**
 * Обобщённый переиспользуемый элемент списка (изначально — перенос строки
 * расписания из приложенного пользователем Schedule.svg, stream.Front#30;
 * генерализован по прямому запросу пользователя в тот же день — видимость
 * (декор, пилюля, орнаменты, блики) не изменилась, изменилась только модель
 * контента). Раньше принимал фиксированный набор доменных inputs
 * (`weekday`/`isOnline`/`eventTitle`/`time`) — теперь принимает произвольный
 * массив `segments()`: каждый сегмент — своя горизонтальная часть строки со
 * своим текстом и (опционально) цветом. Домен (расписание, донатеры, что-то
 * ещё) компонент больше не знает — вызывающий код сам решает, сколько
 * сегментов и какого цвета собрать (например: online-строка расписания —
 * 3 сегмента белым цветом; offline — те же 3 позиции, но средний/правый
 * сегмент собираются с текстом «Оффлайн»/`--:--` и красным `color`).
 *
 * Раскладка сегментов — общий приём: крайние (первый/последний) сегменты не
 * растягиваются (`flex: 0 0 auto`, выровнены к своему краю), любые сегменты
 * между ними делят оставшееся место поровну (`flex: 1 1 0`, по центру) — так
 * при ровно 3 сегментах воспроизводится исходная раскладка
 * weekday/event/time (узкие края, растущая середина), а с другим числом
 * сегментов раскладка остаётся осмысленной без отдельного конфига под каждый
 * сегмент (только `color` — по прямому запросу пользователя, ширина/выравнивание
 * сегментов пока не настраиваются).
 *
 * Декор перенесён 1:1 из Schedule.svg (детали — см. историю компонента до
 * переименования в PROJECT_MAP.md), `id`/`url(#...)` — с `uid`-суффиксом.
 * Ширина сейчас фиксированная (644px) — 9-slice-растягивание отложено.
 */
@Component({
  selector: 'app-list-item',
  imports: [],
  templateUrl: './list-item.html',
  styleUrl: './list-item.scss',
})
export class ListItem {
  protected readonly uid = `listitem${nextListItemUid++}`;

  readonly segments = input.required<ListItemSegment[]>();
}
